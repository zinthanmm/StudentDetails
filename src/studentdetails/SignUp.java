    private javax.swing.JTextField txt_mobile;
    private javax.swing.JTextField txt_name;
    private javax.swing.JPasswordField txt_password;
    private javax.swing.JTextField txt_username;
    // End of variables declaration//GEN-END:variables

    private void saveUser() {
        try {
            User u1 = new User();
            u1.setId(txt_id.getText());
            u1.setName(txt_name.getText());
            u1.setAddress(txtA_address.getText());
            u1.setMobile(txt_mobile.getText());
            u1.setBirthday(combox_date.getSelectedItem().toString()+"."
                    +combox_month.getSelectedItem().toString()+"."
                    +combox_year.getSelectedItem().toString());
            if (rb_male.isSelected()) {
                u1.setGender("Male");
            } else {
                u1.setGender("Female");
            }
            u1.setUsername(txt_username.getText());
            u1.setPassword(txt_password.getText());
            Home.fulldata.add(u1);
            tableLoad();
        } catch (Exception e) {
        }
    }

    private void tableLoad() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) tbl_details.getModel();
            dtm.setRowCount(0);
            for (int i = 0; i < Home.fulldata.size(); i++) {
                User uread = Home.fulldata.get(i);
                Vector v = new Vector();
                v.add(uread.getId());
                v.add(uread.getName());
                v.add(uread.getMobile());
                v.add(uread.getGender());
                dtm.addRow(v);
            }
        } catch (Exception e) {
        }
    }
