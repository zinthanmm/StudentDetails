    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        if (txtA_address.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Please enter the address!", "Message Box", JOptionPane.INFORMATION_MESSAGE);
        }
        if (txt_id.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Please enter the ID number!", "Message Box", JOptionPane.INFORMATION_MESSAGE);
        }
        if (txt_mobile.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Please enter the mobile number!", "Message Box", JOptionPane.INFORMATION_MESSAGE);
        }
        if (txt_name.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Please enter the name!", "Message Box", JOptionPane.INFORMATION_MESSAGE);
        }
        if (txt_password.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Please enter the password!", "Message Box", JOptionPane.INFORMATION_MESSAGE);
        }
        if (txt_username.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Please enter the username!", "Message Box", JOptionPane.INFORMATION_MESSAGE);
        }else{
            saveUser();
        }
    }//GEN-LAST:event_btn_submitActionPerformed

