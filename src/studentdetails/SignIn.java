    private void txt_username2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_username2KeyPressed
        if (evt.getKeyCode()== 10) {
            if (txt_username2.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a valid User Name!", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                txt_password2.grabFocus();
            }
        }
    }//GEN-LAST:event_txt_username2KeyPressed

