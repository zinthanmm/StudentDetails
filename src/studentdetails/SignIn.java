    private void loginToSystem() {
        try {
            for (int i = 0; i < Home.fulldata.size(); i++) {
                User uu = Home.fulldata.get(i);
                if (txt_username2.getText().equals(uu.getUsername()) && txt_password2.getText().equals(uu.getPassword())) {
                    Home.lbl_displayname.setText(uu.getName());
                }
            }
        } catch (Exception e) {
        }
    }
